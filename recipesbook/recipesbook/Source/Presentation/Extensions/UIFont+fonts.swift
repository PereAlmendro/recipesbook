import UIKit

extension UIFont {
    static func helveticaNeueBoldWith(size: CGFloat) -> UIFont? {
        return UIFont(name: "HelveticaNeue-Bold", size: size)
    }
    
    static func helveticaNeueRegularWith(size: CGFloat) -> UIFont? {
        return UIFont(name: "HelveticaNeue-Regular", size: size)
    }
}
